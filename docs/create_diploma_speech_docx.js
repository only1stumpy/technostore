const paragraph = (text) => new Paragraph({
  children: [new TextRun({ text, size: 28, font: 'Times New Roman' })],
  alignment: AlignmentType.JUSTIFIED,
  spacing: { after: 180 },
  indent: { firstLine: 720 }
});

const children = [
  new Paragraph({
    children: [new TextRun({ text: 'Доклад к дипломной презентации', bold: true, size: 36, font: 'Times New Roman' })],
    heading: HeadingLevel.HEADING_1,
    alignment: AlignmentType.CENTER,
    spacing: { after: 240 }
  }),
  new Paragraph({
    children: [new TextRun({ text: 'Тема: «Разработка веб-приложения интернет-магазина электроники TechnoStore»', size: 28, font: 'Times New Roman' })],
    alignment: AlignmentType.CENTER,
    spacing: { after: 360 }
  })
];

slides.forEach((slide, index) => {
  children.push(new Paragraph({
    children: [new TextRun({ text: slide.title, bold: true, size: 30, font: 'Times New Roman' })],
    heading: HeadingLevel.HEADING_2,
    pageBreakBefore: index !== 0,
    spacing: { before: 120, after: 180 }
  }));
  slide.text.forEach((text) => children.push(paragraph(text)));
});

